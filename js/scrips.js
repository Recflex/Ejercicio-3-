var cantidad1 = parseFloat (prompt ('Ingrese el primer numero'));
var cantidad2 = parseFloat (prompt ('Ingrese el segundo numero'));

var suma = cantidad1 + cantidad2;
var resta = cantidad1 - cantidad2;
var multi = cantidad1 * cantidad2;
var div = cantidad1 / cantidad2;

document.write('<p>suma:' +suma+ '</p>');
document.write('<p>resta:' +resta+ '</p>');
document.write('<p>multiplicasión:' +multi+ '</p>');
document.write('<p>División:' +div+ '</p>');